.set UPAGES, (UVPT - PDMAP)
.set UENVS, (UPAGES - PDMAP)

.set UTOP, UENVS
.set UXSTACKTOP, UTOP

.set USTACKTOP, (UTOP - 2 * PTMAP)
.set UTEXT, PDMAP
.set UCOW, (UTEXT - PTMAP)
.set UTEMP, (UCOW - PTMAP)
